int		ft_iterative_factorial(int nb)
{
	int r;

	r = nb;
	if (nb > 12 || nb < 0)
	{
		return (0);
	}
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	else
	{
		while (nb > 1)
		{
			r = r * (nb - 1);
			nb--;
		}
		return (r);
	}
}
